#include "eyes.h"

static Adafruit_SSD1306* displayPtr;

#define EYE 16

void initEyes(Adafruit_SSD1306& display) {
  displayPtr = &display;
}


// === Augenzeichnung ===
void draw_eyes(int left_eye_x, int left_eye_y, int right_eye_x, int right_eye_y) {
  displayPtr->clearDisplay();
  displayPtr->fillRoundRect(left_eye_x - 12, left_eye_y - 10, 24, 20, 4, SSD1306_WHITE);
  displayPtr->fillRoundRect(right_eye_x - 12, right_eye_y - 10, 24, 20, 4, SSD1306_WHITE);
  displayPtr->display();
}

void center_eyes() {
  draw_eyes(40, 32, 88, 32);
}

void blink(int speed) {
  for (int i = 0; i < 2; i++) {
    displayPtr->clearDisplay();
    displayPtr->display();
    delay(speed * 10);
    center_eyes();
    delay(speed * 10);
  }
}

void lookLeft() {
  draw_eyes(35, 32, 83, 32);
}

void lookRight() {
  draw_eyes(45, 32, 93, 32);
}

void saccade() {
  lookLeft();
  delay(150);
  lookRight();
  delay(150);
  center_eyes();
}

